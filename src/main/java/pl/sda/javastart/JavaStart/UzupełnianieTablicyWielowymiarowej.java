package pl.sda.javastart.JavaStart;

public class UzupełnianieTablicyWielowymiarowej {
    //uzupełnianie przekątnej tablicy
    public static void main(String[] args) {
        int[][] tab = new int[3][3];
        tab[0][0] = 5;
        tab[1][1] = 5;
        tab[2][2] = 5;
        System.out.println("tab[][]= " + tab[0][0]);
    }
}
